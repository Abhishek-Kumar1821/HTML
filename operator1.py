a = 10
b = 3

print("Arithmetic Operators")
print("a + b =", a + b)   
print("a - b =", a - b)   
print("a * b =", a * b)   
print("a / b =", a / b)   
print("a // b =", a // b) 
print("a % b =", a % b)   
print("a ** b =", a ** b)

print("\n Relational (Comparison) Operators")
print("a == b :", a == b)
print("a != b :", a != b)
print("a > b  :", a > b)
print("a < b  :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)

print("\n Logical Operators ")
x = True
y = False
print("x and y :", x and y)
print("x or y  :", x or y)
print("not x   :", not x)


print("\n Bitwise Operators ")
print("a & b =", a & b)   
print("a | b =", a | b)   
print("a ^ b =", a ^ b)   
print("~a    =", ~a)      
print("a << 1 =", a << 1) 
print("a >> 1 =", a >> 1) 



print("\n Membership Operators ")
nums = [1, 2, 3, 4, 5]
print("2 in nums :", 2 in nums)
print("10 not in nums :", 10 not in nums)

print("\nIdentity Operators")
m = [1, 2, 3]
n = m
o = [1, 2, 3]
print("m is n :", m is n)       
print("m is o :", m is o)       
print("m is not o :", m is not o)

